// ── Derived portfolio stats ──────────────────────────────────────────────────

export function getPortfolioStats() {
  const activeLeases = properties.flatMap(p => p.leases).filter(
    l => l.status === "active" && l.rate !== null
  );

  const avgRate = activeLeases.length
    ? (activeLeases.reduce((s, l) => s + l.rate, 0) / activeLeases.length).toFixed(2)
    : "—";

  const today = new Date("2026-04-30");
  const expiringLeases = properties.flatMap(p =>
    p.leases
      .filter(l => l.expiration && l.status === "active")
      .map(l => ({ ...l, propertyId: p.id, address: p.address, city: p.city }))
  ).filter(l => {
    const exp = new Date(l.expiration + "-01");
    const monthsAway = (exp - today) / (1000 * 60 * 60 * 24 * 30);
    return monthsAway <= 12 && monthsAway > 0;
  });

  const expiryBuckets = { "2026": 0, "2027": 0, "2028": 0, "2029": 0, "2030+": 0 };
  properties.flatMap(p => p.leases).forEach(l => {
    if (!l.expiration || l.status === "expired") return;
    const yr = l.expiration.split("-")[0];
    if (yr === "2026") expiryBuckets["2026"]++;
    else if (yr === "2027") expiryBuckets["2027"]++;
    else if (yr === "2028") expiryBuckets["2028"]++;
    else if (yr === "2029") expiryBuckets["2029"]++;
    else expiryBuckets["2030+"]++;
  });

  const statusCounts = { active: 0, vacant: 0, mtm: 0, "pre-comm": 0, expired: 0 };
  properties.flatMap(p => p.leases).forEach(l => {
    if (statusCounts[l.status] !== undefined) statusCounts[l.status]++;
  });

  const stateCounts = { TX: 0, AZ: 0, NC: 0, SC: 0 };
  properties.forEach(p => { if (stateCounts[p.state] !== undefined) stateCounts[p.state]++; });

  // WALE: avg months remaining for active leases with known expiration
  const activeWithExp = properties.flatMap(p => p.leases).filter(
    l => l.status === "active" && l.expiration
  );
  const avgMonths = activeWithExp.length
    ? activeWithExp.reduce((s, l) => {
        const exp = new Date(l.expiration + "-01");
        return s + Math.max(0, (exp - today) / (1000 * 60 * 60 * 24 * 30));
      }, 0) / activeWithExp.length
    : 0;
  const wale = (avgMonths / 12).toFixed(1);

  return {
    totalProperties: properties.length,
    avgRate,
    wale,
    expiringLeases,
    expiryBuckets,
    statusCounts,
    stateCounts,
  };
}

export function getPropertyById(id) {
  return properties.find(p => p.id === id);
}

